package src.cars;

public class CarDTO {

	private String reg;
	private String make;
	private String model;
	private int year;
	
	
	public void setReg(String reg) {
		this.reg = reg;
	}
	
	public String getReg() {
		return this.reg;
	}
	
	public void setMake(String make) {
		this.make = make;
	}
	
	public String getMake() {
		return this.make;
	}
	
	public void setModel(String model) {
		this.model = model;
	}
	
	public String getModel() {
		return this.model;
	}
	
	public void setYear(int year) {
		this.year = year;
	}
	
	public int getYear() {
		return this.year;
	}
	
	
}
